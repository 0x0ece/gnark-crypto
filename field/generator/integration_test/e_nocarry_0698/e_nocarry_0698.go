// mulWSigned mul word signed (w/ montgomery reduction)
func (z *e_nocarry_0698) mulWSigned(x *e_nocarry_0698, y int64) {
	m := y >> 63
	_mulWGeneric(z, x, uint64((y^m)-m))
	//multiply by abs(y)
	if y < 0 {
		z.Neg(z)
	}
}

