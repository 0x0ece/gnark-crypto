func (z *e_cios_0320) biggerOrEqualModulus() bool {
	if z[4] > qe_cios_0320[4] {
		return true
	}
	if z[4] < qe_cios_0320[4] {
		return false
	}

	if z[3] > qe_cios_0320[3] {
		return true
	}
	if z[3] < qe_cios_0320[3] {
		return false
	}

	if z[2] > qe_cios_0320[2] {
		return true
	}
	if z[2] < qe_cios_0320[2] {
		return false
	}

	if z[1] > qe_cios_0320[1] {
		return true
	}
	if z[1] < qe_cios_0320[1] {
		return false
	}

	return z[0] >= qe_cios_0320[0]
}

func gen() gopter.Gen {
	return func(genParams *gopter.GenParameters) *gopter.GenResult {
		var g testPaire_cios_0320

		g.element = e_cios_0320{
			genParams.NextUint64(),
			genParams.NextUint64(),
			genParams.NextUint64(),
			genParams.NextUint64(),
			genParams.NextUint64(),
		}
		if qe_cios_0320[4] != ^uint64(0) {
			g.element[4] %= (qe_cios_0320[4] + 1)
		}

		for g.element.biggerOrEqualModulus() {
			g.element = e_cios_0320{
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
			}
			if qe_cios_0320[4] != ^uint64(0) {
				g.element[4] %= (qe_cios_0320[4] + 1)
			}
		}

		g.element.ToBigIntRegular(&g.bigint)
		genResult := gopter.NewGenResult(g, gopter.NoShrinker)
		return genResult
	}
}

func genFull() gopter.Gen {
	return func(genParams *gopter.GenParameters) *gopter.GenResult {

		genRandomFq := func() e_cios_0320 {
			var g e_cios_0320

			g = e_cios_0320{
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
			}

			if qe_cios_0320[4] != ^uint64(0) {
				g[4] %= (qe_cios_0320[4] + 1)
			}

			for g.biggerOrEqualModulus() {
				g = e_cios_0320{
					genParams.NextUint64(),
					genParams.NextUint64(),
					genParams.NextUint64(),
					genParams.NextUint64(),
					genParams.NextUint64(),
				}
				if qe_cios_0320[4] != ^uint64(0) {
					g[4] %= (qe_cios_0320[4] + 1)
				}
			}

			return g
		}
		a := genRandomFq()

		var carry uint64
		a[0], carry = bits.Add64(a[0], qe_cios_0320[0], carry)
		a[1], carry = bits.Add64(a[1], qe_cios_0320[1], carry)
		a[2], carry = bits.Add64(a[2], qe_cios_0320[2], carry)
		a[3], carry = bits.Add64(a[3], qe_cios_0320[3], carry)
		a[4], _ = bits.Add64(a[4], qe_cios_0320[4], carry)

		genResult := gopter.NewGenResult(a, gopter.NoShrinker)
		return genResult
	}
}

