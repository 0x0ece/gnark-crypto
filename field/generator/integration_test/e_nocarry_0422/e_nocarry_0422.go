// Sqrt z = √x mod q
// if the square root doesn't exist (x is not a square mod q)
// Sqrt leaves z unchanged and returns nil
func (z *e_nocarry_0422) Sqrt(x *e_nocarry_0422) *e_nocarry_0422 {
	// q ≡ 1 (mod 4)
	// see modSqrtTonelliShanks in math/big/int.go
	// using https://www.maa.org/sites/default/files/pdf/upload_library/22/Polya/07468342.di020786.02p0470a.pdf

	var y, b, t, w e_nocarry_0422
	// w = x^((s-1)/2))
	w.Exp(*x, _bSqrtExponente_nocarry_0422)

	// y = x^((s+1)/2)) = w * x
	y.Mul(x, &w)

	// b = x^s = w * w * x = y * x
	b.Mul(&w, &y)

	// g = nonResidue ^ s
	var g = e_nocarry_0422{
		16654797091818104285,
		11641747575948091909,
		6414986418399937148,
		3984460965443237098,
		1587437372109897310,
		11967318416542245400,
		46716432416,
	}
	r := uint64(4)

	// compute legendre symbol
	// t = x^((q-1)/2) = r-1 squaring of x^s
	t = b
	for i := uint64(0); i < r-1; i++ {
		t.Square(&t)
	}
	if t.IsZero() {
		return z.SetZero()
	}
	if !((t[6] == 139796825885) && (t[5] == 607163995605406884) && (t[4] == 18439803012845756991) && (t[3] == 982495522523290424) && (t[2] == 677004155419079383) && (t[1] == 11619184878344316770) && (t[0] == 6756246801259639324)) {
		// t != 1, we don't have a square root
		return nil
	}
	for {
		var m uint64
		t = b

		// for t != 1
		for !((t[6] == 139796825885) && (t[5] == 607163995605406884) && (t[4] == 18439803012845756991) && (t[3] == 982495522523290424) && (t[2] == 677004155419079383) && (t[1] == 11619184878344316770) && (t[0] == 6756246801259639324)) {
			t.Square(&t)
			m++
		}

		if m == 0 {
			return z.Set(&y)
		}
		// t = g^(2^(r-m-1)) mod q
		ge := int(r - m - 1)
		t = g
		for ge > 0 {
			t.Square(&t)
			ge--
		}

		g.Square(&t)
		y.Mul(&y, &t)
		b.Mul(&b, &g)
		r = m
	}
}

